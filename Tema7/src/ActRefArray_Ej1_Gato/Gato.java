package ActRefArray_Ej1_Gato;

public class Gato {
    String noombre;
    String raza;
    String color;

    public Gato(String noombre, String raza, String color) {
        this.noombre = noombre;
        this.raza = raza;
        this.color = color;
    }

    public String getNoombre() {
        return noombre;
    }

    public void setNoombre(String noombre) {
        this.noombre = noombre;
    }

    public String getRaza() {
        return raza;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
    
}
